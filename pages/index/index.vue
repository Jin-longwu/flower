	import uniPopupMessage from '@/components/uni-popup/uni-popup-message.vue'
	import uniPopupDialog from '@/components/uni-popup/uni-popup-dialog.vue'
	import uniGoodsNav from '@/components/uni-ui/uni-goods-nav/uni-goods-nav.vue'
	import uniList from "@/components/uni-ui/uni-list/uni-list.vue";
	import uniListItem from "@/components/uni-ui/uni-list-item/uni-list-item.vue";
	import {
		formatRichText
	} from "@/utils/format.js"
	import parse from '@/utils/htmlparser.js';
	import {
		myRequestGet
	} from '@/utils/zgrequest.js'
	export default {
		data() {
			return {
				message: [],
				content: {},
				htmlNodes: [],
				// info: {},
				options: [{
					icon: 'shop',
					text: '店铺',
					infoBackgroundColor: '#007aff',
					infoColor: "red"
				}, {
					icon: 'cart',
					text: '购物车',
					info: 1
				}],
				buttonGroup: [{
						text: '加入购物车',
						backgroundColor: '#DDF2EC',
						color: '#A4DCCD',
						info: 0
					},
					{
						text: '立即购买',
						backgroundColor: '#65C4AA',
						color: '#fff'
					}
				]
			}
		},
		onLoad() {
			this.getmessage(),
				this.getcontent()
		},
		methods: {
			open(){
			         this.$refs.popup.open()
			      },
			async getmessage() {
				let result = await myRequestGet("/wscgoods/weapp/detail.json?alias=26xr56g4mjbq2")
				this.message = result.data
			},
			async getcontent() {
				let result1 = await myRequestGet("/wscshop/goods/showcase-components.json?alias=26xr56g4mjbq2&kdt_id=10056586")
				this.content = formatRichText(result1.data.components[0].content)
				//#ifdef MP-ALIPAY
				this.htmlNodes = parse(this.content)
				//#endif

			},
			previewImg(current) {
				const urls = this.message.goodsData.goods.pictures.map(item => {
					return item.url
				})
				uni.previewImage({
					current,
					urls
				})
			},
			onClick(e) {
				uni.showToast({
					title: `点击${e.content.text}`,
					icon: 'none'
				})
			},
			buttonClick(e) {
				console.log(e)
				this.options[1].info++
			}
		},
		components: {
			uniList,
			uniListItem,
			uniGoodsNav,
			uniPopup,
			uniPopupMessage,
			uniPopupDialog
		}
	}
</script>

